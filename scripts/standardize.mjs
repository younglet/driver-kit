function normalizeTitle(text) {
  // 去括号、空格、顿号等差异
  const cleaned = text.replace(/[（）()]/g, '').replace(/\s+/g, '').replace(/、/g, '')
  if (NORMALIZED[text]) return NORMALIZED[text]
  if (NORMALIZED[cleaned]) return NORMALIZED[cleaned]
  // 模糊匹配
  for (const [key, val] of Object.entries(NORMALIZED)) {
    const cleanedKey = key.replace(/[（）()]/g, '').replace(/\s+/g, '').replace(/、/g, '')
    if (cleanedKey === cleaned) return val
  }
  return null
}

// ════════════════════════════════════════════════════════════
//  3. 解析 + 重排（保留一切）
// ════════════════════════════════════════════════════════════
function parseMarkdown(content) {
  const lines = content.split('\n')
  let frontmatter = ''
  let bodyStart = 0
  if (lines[0]?.trim() === '---') {
    for (let i = 1; i < lines.length; i++) {
      if (lines[i].trim() === '---') {
        frontmatter = lines.slice(0, i + 1).join('\n') + '\n'
        bodyStart = i + 1
        break
      }
    }
  }

  const sections = []
  let currentSection = null
  let introLines = []

  for (let i = bodyStart; i < lines.length; i++) {
    const line = lines[i]
    const titleMatch = line.match(/^## (\d+)\. (.+)$/)
    if (titleMatch) {
      if (currentSection) sections.push(currentSection)
      currentSection = {
        number: parseInt(titleMatch[1]),
        originalTitle: titleMatch[2].trim(),
        content: [line],
      }
    } else {
      if (currentSection) currentSection.content.push(line)
      else introLines.push(line)
    }
  }
  if (currentSection) sections.push(currentSection)

  return { frontmatter, introLines, sections }
}

function buildSections(sections) {
  // 对每个章节归一化
  const result = []
  const stdSeen = new Map() // 标准化标题 → 已添加的第一个章节

  for (const sec of sections) {
    const norm = normalizeTitle(sec.originalTitle)
    if (norm && STANDARD_TEMPLATE.includes(norm)) {
      // 标准章节
      if (!stdSeen.has(norm)) {
        // 第一次出现：保留
        stdSeen.set(norm, sec)
        sec.normalizedTitle = norm
        sec.isStandard = true
        result.push(sec)
      } else {
        // 重复出现：合并内容到第一个
        const first = stdSeen.get(norm)
        // 只取重复章节的正文部分（跳过原标题行）
        const dupContent = sec.content.slice(1).join('\n').trim()
        if (dupContent) {
          first.content.push('\n\n' + dupContent + '\n')
        }
      }
    } else {
      // 非标准（附加）章节：保留
      sec.normalizedTitle = sec.originalTitle
      sec.isStandard = false
      result.push(sec)
    }
  }
  return result
}

function renumberAndSort(sections) {
  // 拆成标准 + 附加
  const standard = []
  const extras = []
  for (const sec of sections) {
    if (sec.isStandard) standard.push(sec)
    else extras.push(sec)
  }

  // 标准章节按模板顺序
  const byTitle = new Map()
  for (const sec of standard) {
    if (!byTitle.has(sec.normalizedTitle)) byTitle.set(sec.normalizedTitle, sec)
  }

  const result = []
  let num = 1
  for (const title of STANDARD_TEMPLATE) {
    if (byTitle.has(title)) {
      const sec = byTitle.get(title)
      sec.number = num++
      result.push(sec)
    }
  }
  // 附加章节：保持原顺序，编号从 num 开始
  for (const sec of extras) {
    sec.number = num++
    result.push(sec)
  }
  return result
}

function rebuildMarkdown(parsed, sections) {
  let out = parsed.frontmatter
  if (parsed.introLines.length > 0) {
    out += parsed.introLines.join('\n')
  }
  for (const sec of sections) {
    const title = sec.normalizedTitle
    out += '\n\n## ' + sec.number + '. ' + title + '\n'
    out += sec.content.slice(1).join('\n')
  }
  return out
}

// ════════════════════════════════════════════════════════════
//  主入口
// ════════════════════════════════════════════════════════════
function processFile(file) {
  const content = fs.readFileSync(file, 'utf8')
  const parsed = parseMarkdown(content)
  const built = buildSections(parsed.sections)
  const reordered = renumberAndSort(built)
  const rebuilt = rebuildMarkdown(parsed, reordered)

  return {
    file,
    changed: rebuilt !== content,
    content,
    updated: rebuilt,
    sections: reordered,
  }
}

function main() {
  if (!fs.existsSync(DOCS_DIR)) {
    console.error(`docs 目录不存在: ${DOCS_DIR}`)
    process.exit(1)
  }

  const files = fs
    .readdirSync(DOCS_DIR)
    .filter(
      (f) =>
        f.endsWith('.md') &&
        !f.match(/^(index|分类总览|编写规范|README)\.md$/) &&
        !/^(传感器|执行器|通信|显示|输入|存储|电源|时钟)\.md$/.test(f)
    )
    .map((f) => path.join(DOCS_DIR, f))

  console.log(`📂 找到 ${files.length} 个模块文档\n`)
  if (REPORT_ONLY) {
    console.log('📋 仅报告模式\n')
  } else if (DRY_RUN) {
    console.log('🔍 Dry-run 模式（不修改文件）\n')
  } else {
    console.log('✏️  执行模式（将修改文件）\n')
  }

  let totalChanged = 0
  let totalLinesPreserved = 0
  let totalLinesOriginal = 0
  const report = []

  for (const file of files) {
    const result = processFile(file)
    totalLinesOriginal += result.content.split('\n').length
    totalLinesPreserved += result.updated.split('\n').length

    if (result.changed) {
      totalChanged++
      if (!REPORT_ONLY && !DRY_RUN) {
        fs.writeFileSync(file, result.updated)
      }
      const extras = result.sections.filter((s) => !s.isStandard).length
      report.push({
        file: path.relative(DOCS_DIR, file),
        total: result.sections.length,
        extras,
      })
    }
  }

  console.log('═'.repeat(60))
  console.log(`📊 汇总`)
  console.log('═'.repeat(60))
  console.log(`  文档总数：${files.length}`)
  console.log(`  修改文档：${totalChanged}`)
  console.log(`  原行数：${totalLinesOriginal}`)
  console.log(`  现行数：${totalLinesPreserved}`)
  console.log(`  差异：${totalLinesPreserved - totalLinesOriginal} 行（合并重复标题）`)

  // 显示有附加章节的文档
  const withExtras = report.filter((r) => r.extras > 0)
  if (withExtras.length > 0) {
    console.log('')
    console.log('📎 含有附加章节的文档（保留原标题，归入附加章节）：')
    for (const r of withExtras.slice(0, 20)) {
      console.log(`  ${r.file}: ${r.total} 节（${r.extras} 个附加）`)
    }
    if (withExtras.length > 20) {
      console.log(`  ... 还有 ${withExtras.length - 20} 个`)
    }
  }

  if (DRY_RUN || REPORT_ONLY) {
    console.log('')
    console.log('💡 使用 `node scripts/standardize.mjs` 真正执行修改')
  } else {
    console.log('')
    console.log('✅ 完成。')
  }
}

main()